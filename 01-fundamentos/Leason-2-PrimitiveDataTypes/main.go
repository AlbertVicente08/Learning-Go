package main

import "fmt"

func main() {

	//Cadena de textos
	var mistring string
	//String zero values is ""
	fmt.Println(mistring)
	mistring = "Hola y bienvenido a code and learn"
	fmt.Println(mistring)


	//Numero enteros
	var miint int
	//Zero value for number types is 0
	fmt.Println(miint)
	miint = 22
	fmt.Println(miint)


	//Boleanos 
	var miboleano bool
	//zero value for bool is false
	fmt.Println(miboleano)
	miboleano = true
	fmt.Println(miboleano)
}
